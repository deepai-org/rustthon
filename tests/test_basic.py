# Basic arithmetic
print(1 + 2)
print(10 - 3)
print(4 * 5)
print(10 / 3)

# Variables
x = 42
y = 8
print(x + y)

# Strings
name = "Rustthon"
print(name)

# Boolean
print(True)
print(False)
print(1 > 0)
print(1 == 2)

# Lists
nums = [1, 2, 3, 4, 5]
print(nums)

# Conditionals
if x > 10:
    print("x is big")
else:
    print("x is small")

# While loop
i = 0
while i < 5:
    print(i)
    i = i + 1

# Nested expressions
result = (x + y) * 2
print(result)

